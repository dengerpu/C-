#include <stdio.h>
int main() {
	//2.求出0～999之间的所有“水仙花数”并输出。
	//“水仙花数”是指一个三位数，
	//其各位数字的立方和确好等于该数本身，
	//如；153＝1＋5＋3 ? ，则153是一个“水仙花数”。
	for (int i = 100; i <= 999; i++) {
		int a = i % 10; //个位
		int b = i / 10 % 10;//十位
		int c = i / 100;
		if (i == (a * a * a + b * b * b + c * c * c)) {
			printf("%d ", i);
		}
	}

	return 0;
}